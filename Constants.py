# The Python math library gives us access to constant mathematical variables as well as mathematical functions such as
# Euler's Number, Pi, Tau,NAN and Infinity

# Start by importing the math library
import math
pi = math.pi
print(pi)

tau = math.tau
print(tau)

euler = math.e
print(euler)

# Abstract constant  -> These include NAN (non-constant) and INF (infinity) which are different from the three above
non_constant = math.nan
print(non_constant)

infinity = math.inf
print(infinity)






